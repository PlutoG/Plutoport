class Hook_exists_catalogue
{

	/**
	 * Standard modular run function for snippet hooks. Generates XHTML to insert into a page using AJAX.
	 *
	 * @return tempcode  The snippet
	 */
	function run()
	{
		$val=get_param('name');

		$test=$GLOBALS['SITE_DB']->query_value_null_ok('catalogues','c_name',array('c_name'=>$val));
		if (is_null($test)) return new ocp_tempcode();

		return make_string_tempcode(strip_html(do_lang('ALREADY_EXISTS',escape_html($val))));
	}

}
