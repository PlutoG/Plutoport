class Hook_notes_quotes
{

	/**
	 * Standard modular run function. See if we have to decache based on the passed filename.
	 *
	 * @param PATH		Filename.
	 */
	function run($file)
	{
		if ((strpos($file,'/quotes')!==false) || ($file=='quotes')) decache('main_quotes');
	}

}


